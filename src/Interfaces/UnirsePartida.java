    private void btnUnirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnirActionPerformed

        String server = txtIPserver.getText();
        this.nombre = txtNombre.getText();
        int puerto = -1;
        try {
            puerto = Integer.parseInt(txtPuerto.getText());
        } catch (NumberFormatException e) {
            
                        JOptionPane.showMessageDialog(
                    null,
                    "El numero del puerto no es valido",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            
            
            return;
        }
        

        player = new JugadorUNO();

        if (!player.Conectar(server, puerto)) {

            JOptionPane.showMessageDialog(
                    null,
                    "Verifique el puerto o la IP",
                    "Error con la conexi√≥n",
                    JOptionPane.ERROR_MESSAGE
            );

            return;
        }

        player.setName(this.nombre);

        Interfaz UI = new Interfaz();

        UI.setJugador(player);
        player.setInterfaz(UI);

        JsonObject json = new JsonObject();
        json.addProperty("Tipo", "NuevoJugador");
        json.addProperty("Nombre", this.nombre);

        String mensaje = json.toString();

        player.enviarMensaje(mensaje);

        UI.setVisible(true);

        this.dispose();
    }//GEN-LAST:event_btnUnirActionPerformed

